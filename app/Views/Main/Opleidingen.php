            <h2>Een opleiding volgen bij Koskamp</h2>
            <p>Koskamp helpt u graag om de kwaliteit en kennis in uw bedrijf te vergroten en op peil te houden. Door de juiste kennis en vaardigheden binnen uw bedrijf op niveau te houden, kunt u uw klanten zo goed mogelijk van dienst zijn en een uitdagende concurrent blijven in deze snel veranderende maatschappij, zowel op technisch als commercieel vlak.&nbsp;</p>
            <h3>Voordelen van een opleiding via Koskamp</h3> Reden om te kiezen voor een opleidingsprogramma waarin continu wordt getraind op systematisch denken en werken. In het opleidingsprogramma wordt middels een niveautest het startniveau gemeten waardoor alleen trainingen die noodzakelijk zijn worden geadviseerd. Er is geen voorschrift voor het merk testapparatuur dat u gebruikt in uw werkplaats.
            <p><strong>Waar bevinden de opleidingscentra van Koskamp?</strong></p>
            <ul>
                <li>Assen</li>
                <li>Den Ham</li>
                <li>Nijmegen</li>
                <li>Steenwijk</li>
            </ul>
            <p><strong>Een aantal voordelen op rij:</strong></p>
            <ul>
                <li>Trainen in de regio</li>
                <li>Een vaste trainer uit de praktijk</li>
                <li>Trainingstijden van 14.00 - 21.00 uur</li>
                <li>Planning en online training</li>
                <li>Persoonlijk opleidingsplan op maat d.m.v. de niveautoets</li>
                <li>Minder verleturen door werknemers door inzet van online training</li>
                <li>Training uit het opleidingsplan van Koskamp worden (deels) geaccordeerd door BOSCH</li>
                <li>De opleidingen worden volledig verzorgd in één van onze opleidingscentra</li>
            </ul>
